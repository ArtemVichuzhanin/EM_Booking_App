import SwiftUI

enum Fonts {
    enum Price {
        static let primary = Font.system(size: 30, weight: .semibold)
        static let secondary = Font.system(size: 16, weight: .regular)
    }

    static let titleMedium = Font.system(size: 22, weight: .medium)
    static let headlineRegular = Font.system(size: 17, weight: .regular)
    static let textMedium = Font.system(size: 16, weight: .medium)
    static let textRegular = Font.system(size: 16, weight: .regular)
    static let bodyMedium = Font.system(size: 14, weight: .medium)
    static let bodyRegular = Font.system(size: 14, weight: .regular)
    static let calloutRegular = Font.system(size: 12, weight: .regular)
}
